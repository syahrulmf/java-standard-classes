package classes;

public class MathClass {
  public static void main(String[] args) {

    /**
     *
     * Method di Math Class :
     * [1] double cos(double) -> Menghitung cos di trigonometri
     * [2] double sin(double) -> Menghitung sin di trigonometri
     * [3] double tan(double) -> Menghitung tan di trigonometri
     * [4] min(number1, number2) -> Mengambil nilai terkecil
     * [5] max(number1, number2) -> Mengambil nilai terbesar
     * ...dan masih banyak lagi
     *
     */

    var min = Math.min(1000, 2000);
    System.out.println(min);

    var max = Math.max(1000, 2000);
    System.out.println(max);

  }
}
